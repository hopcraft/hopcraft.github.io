function loadUserKeys(){
	keys = [];
	keys.push(parseInt(localStorage.getItem("key_left")));
	keys.push(parseInt(localStorage.getItem("key_right")));
	keys.push(parseInt(localStorage.getItem("key_rotate_CW")));
	keys.push(parseInt(localStorage.getItem("key_rotate_CCW")));
	keys.push(parseInt(localStorage.getItem("key_soft_drop")));
	keys.push(parseInt(localStorage.getItem("key_hard_drop")));
	keys.push(parseInt(localStorage.getItem("key_hold")));
	keys.push(parseInt(localStorage.getItem("key_confirm")));
	keys.push(parseInt(localStorage.getItem("key_cancel")));
	return keys;
}

function saveUserKeys(newValues){
	if(newValues.length != 9){
		return false;
	}

	i = 0;
	localStorage.setItem ("key_left", newValues[i]);
	i++;
	localStorage.setItem ("key_right", newValues[i]);
	i++;
	localStorage.setItem ("key_rotate_CW", newValues[i]);
	i++;
	localStorage.setItem ("key_rotate_CCW", newValues[i]);
	i++;
	localStorage.setItem ("key_soft_drop", newValues[i]);
	i++;
	localStorage.setItem ("key_hard_drop", newValues[i]);
	i++;
	localStorage.setItem ("key_hold", newValues[i]);
	i++;
	localStorage.setItem ("key_confirm", newValues[i]);
	i++;
	localStorage.setItem ("key_cancel", newValues[i]);
	console.log("user Keys saved");
	return true;
}

